package com.yuyi.model;

public class Car {
	private String car_id;
	private String car_name;   //车辆名称
	private String car_length; //车辆长度
	private String car_vin; //车架号
	private String car_number;   //车牌号
	private String car_driver; //车辆所属驾驶员
	private String car_unit;   //车辆所属单位
	public String getCar_id() {
		return car_id;
	}
	public void setCar_id(String car_id) {
		this.car_id = car_id;
	}
	public String getCar_name() {
		return car_name;
	}
	public void setCar_name(String car_name) {
		this.car_name = car_name;
	}
	public String getCar_length() {
		return car_length;
	}
	public void setCar_length(String car_length) {
		this.car_length = car_length;
	}
	public String getCar_vin() {
		return car_vin;
	}
	public void setCar_vin(String car_vin) {
		this.car_vin = car_vin;
	}
	public String getCar_number() {
		return car_number;
	}
	public void setCar_number(String car_number) {
		this.car_number = car_number;
	}
	public String getCar_driver() {
		return car_driver;
	}
	public void setCar_driver(String car_driver) {
		this.car_driver = car_driver;
	}
	public String getCar_unit() {
		return car_unit;
	}
	public void setCar_unit(String car_unit) {
		this.car_unit = car_unit;
	}
}
