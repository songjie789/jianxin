package com.yuyi.model;

public class Unit {
	private Integer id;
	private String unit_name;
	private String unit_address;
	private String unit_contact;
	private String unit_phone;
	
	/**
	 * @return the id
	 */
	public Integer getId() {
	    return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
	    this.id = id;
	}
	/**
	 * @return the unit_name
	 */
	public String getUnit_name() {
	    return unit_name;
	}
	/**
	 * @param unit_name the unit_name to set
	 */
	public void setUnit_name(String unit_name) {
	    this.unit_name = unit_name;
	}
	/**
	 * @return the unit_address
	 */
	public String getUnit_address() {
	    return unit_address;
	}
	/**
	 * @param unit_address the unit_address to set
	 */
	public void setUnit_address(String unit_address) {
	    this.unit_address = unit_address;
	}
	/**
	 * @return the unit_contact
	 */
	public String getUnit_contact() {
	    return unit_contact;
	}
	/**
	 * @param unit_contact the unit_contact to set
	 */
	public void setUnit_contact(String unit_contact) {
	    this.unit_contact = unit_contact;
	}
	/**
	 * @return the unit_phone
	 */
	public String getUnit_phone() {
	    return unit_phone;
	}
	/**
	 * @param unit_phone the unit_phone to set
	 */
	public void setUnit_phone(String unit_phone) {
	    this.unit_phone = unit_phone;
	}
	
}
